# Q2) Decision
# If statements that run dependent on user input.

decision = input("Who sees me rolling? (cops, wardens, other)")

if decision == "cops":
    print("They see me rolling. I am ashamed of myself.  I have to apologise to them!.")

elif decision == "wardens":
    print("My music's so loud. I'm was distracted. They reprimanded me")

else:
    print("The warden was just smiling!")